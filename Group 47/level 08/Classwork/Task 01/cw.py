# comparison operators
print(5 > 3)   # True
print(7 > 10)  # False

print(5 < 3)   # False
print(2 < 8)   # True

print(5 >= 3)  # True
print(7 >= 7)  # True

print(5 <= 3)  # False
print(3 <= 3)  # True

print(5 == 5)  # True
print(3 == 7)  # False

print(5 != 3)  # True
print(3 != 3)  # False