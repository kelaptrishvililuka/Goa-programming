# 3. შექმენით 2 ცვლადე სადაც ერთში შეინახავთ True-ს და მეორეში False-ს. შემდეგ კი დაპრინტეთ ლოგიკური ოპერატორების და ამ ცვლადების გამოყენებით ყველა შესაძლო განსხვავებული ვარიანტი.

a = True
b = False
print("a and b:", a and b)  # AND operator
print("a or b:", a or b)    # OR operator
print("not a:", not a)      # NOT operator (a)
print("not b:", not b)      # NOT operator (b)