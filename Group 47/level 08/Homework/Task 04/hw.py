# 4. მომხმარებელს შემოატანინეთ 2 ინტეჯერ ტიპის მონაცემი, შეინახეთ ისინი ცვლადებში და საბოლოოდ შეადარეთ ეს 2 რიცხვი ყველა შედარების ოპერატორით (==, <, >, <=, >=, !=).


number1 = int(input("შეიყვანეთ პირველი რიცხვი: "))
number2 = int(input("შეიყვანეთ მეორე რიცხვი: "))
print("number1 == number2:", number1 == number2)  # equality
print("number1 < number2:", number1 < number2)    # is less
print("number1 > number2:", number1 > number2)    # there is more
print("number1 <= number2:", number1 <= number2)  # is less than or equal to
print("number1 >= number2:", number1 >= number2)  # is greater than or equal to
print("number1 != number2:", number1 != number2)  # not equal