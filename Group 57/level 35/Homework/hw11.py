# 11) შექმენით ფუნქცია რომელიც მომხმარებელს შეეკითხება რიცხვს შემდეგ კი ერთიდან იმ რიცხვამდე დაბეჭდავს ყველა ლუწ რიცხვს.

def nums():
    x = int(input("Enter a Number: "))  # მომხმარებელს რიცხვის შეყვანა სთხოვს და მას x-ში ინახავს.
    for i in range(2, x + 1, 2):  # ციკლი იწყება 2-დან, გადადის x-მდე და ყოველ ჯერზე 2-ს ამატებს.
        print(i)  # ბეჭდავს i-ს (ანუ ყველა ლუწ რიცხვს 2-დან x-მდე).
    
nums()  # ფუნქციის გამოძახება, რომ პროგრამა შესრულდეს.