# 12) შექმენით ფუნქცია რომელიც მომხმარებელს შეეკითხება რიცხვს შემდეგ კი ერთიდან იმ რიცხვამდე დაბეჭდავს ყველა კენტ რიცხვს.

def odd_numbers():  # ფუნქციის შექმნა
    x = int(input("Enter a number: "))  # მომხმარებელს რიცხვის შეყვანა სთხოვს და მას x-ში ინახავს
    for i in range(1, x + 1, 2):  # ციკლი 1-დან იწყება, x-მდე მიდის და ყოველ ჯერზე 2-ს ამატებს
        print(i)  # ბეჭდავს i-ს (ანუ ყველა კენტ რიცხვს)

odd_numbers()  # ფუნქციის გამოძახება, რომ კოდი შესრულდეს.